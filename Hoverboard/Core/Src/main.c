void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM1)
	{
		Angle_Period++;
		if(Angle_Period>=10)
		{
			Angle_Period=0;
			MPU6050_GetData(&AX,&AY,&AZ,&GX,&GY,&GZ);
			AngleACC=atan2(AX,AZ)/3.14159*180;
			AngleGYR=Angle-GY/32768.0*2000*0.01;//0.01是10ms读一次了，变换周期时有一些参数要对应修改
			static float A=0.01;
			//Angle=(1-A)*AngleGYR+A*AngleACC;
			Angle =AngleGYR+A*(AngleACC-AngleGYR);
		}
	}
}

