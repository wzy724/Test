/* USER CODE BEGIN 1 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM3&&htim->Channel==HAL_TIM_ACTIVE_CHANNEL_3)
	{
		ICTime=__HAL_TIM_GET_COMPARE(&htim3,TIM_CHANNEL_3);
		__HAL_TIM_SET_COUNTER(&htim3,0);

		if(ICTime>12000&&ICTime<15000)
		{
			 Data=0;
			 DataCnt=0;
		}
		else if(ICTime>900&&ICTime<1300)
		{
			Data>>=1;
			DataCnt+=1;
		}
		else if(ICTime>2000&&ICTime<2500)
		{
			Data>>=1;
			Data|=(0x80000000);
			DataCnt+=1;
		}
		if(DataCnt==32)
		{
			uint8_t Add = (Data)&(0xFF);
			uint8_t Addi= (Data>>8)&(0xFF);
			uint8_t Cmd = (Data>>16)&(0xFF);
			uint8_t Cmdi= (Data>>24)&(0xFF);

			if(Add==(uint8_t)~Addi&&Cmd==(uint8_t)~Cmdi)//先对提升为 int 的 addr_inv 取反（得到 4 字节的错误值）；
														//强转为 uint8_t（1 字节），截断高 24 位，只保留低 8 位；
			{
				EncodeFlag=1;
			}
				DataCnt=0;
		}

	}
}
