/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==USART1)
	{

		HAL_UART_Transmit(&huart1, &RX_Data, 1,HAL_MAX_DELAY);
		switch(RX_State)
		{
			case 0:
			if(RX_Data=='@')
			{
				RX_Index=0;
				memset(RX_String,0,RX_MaxLength);
				RX_State=1;
			}
			break;

			case 1:
			if(RX_Data=='\r')
			{
				RX_State=2;
			}
			else if(RX_Index>=RX_MaxLength-1)
			{
				RX_Index=0;
				RX_State=0;
			}
			else
			{
				RX_String[RX_Index]=RX_Data;
				RX_Index++;
			}
			break;

			case 2:
			if(RX_Data=='\n')
			{
				RX_String[RX_Index]='\0';
				RX_Flag=1;
			}
			RX_Index=0;
			RX_State=0;
			break;
		}
		HAL_UART_Receive_IT(&huart1, &RX_Data, 1);
	}
}
